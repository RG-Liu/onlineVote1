package com.qst.onlineVote.pojo;

/**
存储每个选项对应的票数
*/

public class Option {
private String option;
private int num;
public String getOption() {
	return option;
}
public void setOption(String option) {
	this.option = option;
}
public int getNum() {
	return num;
}
public void setNum(int num) {
	this.num = num;
}

}

